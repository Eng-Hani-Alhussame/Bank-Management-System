CREATE PROCEDURE DepositAmount
    @PhoneNumber NVARCHAR(20),
    @Amount DECIMAL(18,2)
AS
BEGIN
    SET NOCOUNT ON;
    BEGIN TRY
        BEGIN TRANSACTION;

        IF NOT EXISTS (SELECT 1 FROM Customers WHERE PhoneNumber = @PhoneNumber)
        BEGIN
            RAISERROR('العميل غير موجود', 16, 1);
            ROLLBACK TRANSACTION;
            RETURN;
        END

        UPDATE Customers
        SET Balance = Balance + @Amount
        WHERE PhoneNumber = @PhoneNumber;

        INSERT INTO Transactions (AccountNumber, PhoneNumber, TransactionType, Amount)
        SELECT AccountNumber, PhoneNumber, 'Deposit', @Amount
        FROM Customers
        WHERE PhoneNumber = @PhoneNumber;

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        ROLLBACK TRANSACTION;
        THROW;
    END CATCH
END


