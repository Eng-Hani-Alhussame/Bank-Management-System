CREATE PROCEDURE TransferAmount
    @SenderPhone NVARCHAR(20),
    @ReceiverPhone NVARCHAR(20),
    @Amount DECIMAL(18,2)
AS
BEGIN
    SET NOCOUNT ON;

    BEGIN TRY
        BEGIN TRANSACTION;

        -- تحقق أن الرصيد يكفي
        IF EXISTS (SELECT 1 FROM Customers WHERE PhoneNumber = @SenderPhone AND Balance >= @Amount)
        BEGIN
            -- خصم من المرسل
            UPDATE Customers
            SET Balance = Balance - @Amount
            WHERE PhoneNumber = @SenderPhone;

            -- إضافة للمستلم
            UPDATE Customers
            SET Balance = Balance + @Amount
            WHERE PhoneNumber = @ReceiverPhone;

            -- تسجيل الحركة
            INSERT INTO Transactions (AccountNumber, PhoneNumber, TransactionType, Amount, ReceiverAccount, ReceiverPhone)
            SELECT s.AccountNumber, s.PhoneNumber, 'Transfer', @Amount, r.AccountNumber, r.PhoneNumber
            FROM Customers s
            JOIN Customers r ON r.PhoneNumber = @ReceiverPhone
            WHERE s.PhoneNumber = @SenderPhone;

            COMMIT TRANSACTION;
        END
        ELSE
        BEGIN
            RAISERROR('الرصيد غير كافٍ للتحويل', 16, 1);
            ROLLBACK TRANSACTION;
        END
    END TRY
    BEGIN CATCH
        ROLLBACK TRANSACTION;
        THROW;
    END CATCH
END




